        	<h3>Introduction of Pebble</h3>
            <p>
Pebble is a co-product of yellow sand exploitation. As a kind of natural stone. Pebble is taken from sand and gravel in the mountains produced by ancient riverbed uplift which after crustal movement of thousands of years ago, constantly be squeezed and friction by mountain torrent shock, water transport and washing process. Main pebble products include: natural color pebbles, river pebbles, stone, dry stick stone, stencilled stone, landscaping stone, petrified wood, architectural and cultural materials, non-toxic, tasteless, colorstay. Quality hard, sharp color quaint, with characteristic of natural stone compressive strength, corrosion resistant properties, pebble is an ideal green building materials.
			</p>
        	<h3>Technological Process of Pebble</h3>
            <p>
As the rapid development of construction industry, and accelerating of urbanization process, the market showed an increasing demand for construction of gravel. But because of the rocks and cobblestone geographical resources distribution, led to the parts of shortage of nature sand and gravel, then people have to favour the exploitation and application of artificial cobblestone. The global most countries have begun to use pebbles or crushed pebble as raw materials used in first-class highway construction and the city's construction industry. We often use pebbles and crushed cobblestone as the cushion graded gravel, cement stabilized gravel subbase materials in many highways construction, most of the bridge structure buildings, and pavement engineering structures. After lots of experiments show that crushed cobblestone is a combination of the double advantages of nature pebbles and artificial stone, can not only improve the physical indicators of cement concrete, but also making cement concrete no longer cracks as easy contraction. So it can be referred to as the "green environmental protection building materials". The cobblestone crusher is often used for primary crushing or secondary crushing in quarry stone crushing process.Winnermac pebble crusher adopts the most advanced international crushing technology and manufacturing level, mainly dominated by crushed cobblestone and other hard rock, especially for crushing hard and strong abrasive materials specification in 3-7 cm. Compared with the traditional crusher, whether the high service life, low repair rate and low failure rate reflects its incomparable superiority. Cobble crushing production line consists of vibrating feeder, jaw crusher, impact crusher, vibrating screen, belt conveyor, concentrated electronic equipment, according to different technical requirements, various types of equipment undertakes assorted, meet customer's different technological requirements.			</p>			
