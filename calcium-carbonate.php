        	<h3>Introduction of Calcium Carbonate</h3>
            <p>
Calcium carbonate is a kind of inorganic compounds, commonly known as grey stone, limestone, marble, etc. Its chemical formula is CaCO3, PH value neutral, insoluble in water, soluble in hydrochloric acid. Calcium carbonate is a type of common mineral substance on earth, exists in aragonite, calcite, chalk, limestone, marble, travertine and other rocks, also is the main component of the animal bones or shell. Calcium carbonate is generated by the combination of calcium ions and carbonate ions, so it is calcium+carbonate. Calcium carbonate is divided into two types: heavy calcium carbonate and light calcium carbonate. Due to the heavy calcium carbonate and precipitated calcium carbonateâ€™s different nature of particle size, crystal and interface, the features of reaction is also vastly different. To this end, it extends many different varieties and different uses of calcium carbonate species, such as the activity of calcium carbonate, calcium carbonate for paper making, calcium carbonate for plastics, nanometer calcium carbonate, consumption of calcium carbonate, medicinal calcium carbonate, chemical calcium carbonate, coating calcium carbonate and so on.
        	<h3>Technological Process of Calcium Carbonate</h3>
            <p>
After being screened to remove gangue, raw materials will be delivered into stone crusher for coarse crushing, then to grinding mill for fine grinding to get micro calcium carbonate powder., finish through classifier for grading. The parts of powder which particle size conformed to the requirements will been packaged and warehoused as final products; other part do not conform to the specifications will be returned to calcium carbonate powder mill for grinding again. A complete set of calcium carbonate powder grinding machine is mainly composed of mill host, classifier, blower fan, jaw crusher, winnowing pan hoist, electromagnetic vibrating feeder, storage silo bin, bag-type dust collector, pipe device, cyclone powder machine, electric control cabinet, motor, etc. Calcium carbonate grinder machine is of vertical structure, cover area small, and host transmission device adopts enclosed gear box, smooth transmission. The mill adopts centralized control electrical system, advanced and reasonable shape, high degree of automation; vibrating feeder with small volume and light weight, easy to adjust and save electricity.
			</p>			
        	<h3>Application Fields of Calcium Carbonate</h3>
            <p>
Calcium carbonate is often used in construction industry and paper industry, as additive in food industry. Calcium carbonate within 200 mesh can be used for various feed supplement that calcium content of more than 55.6% without harmful ingredients. 250 mesh to 300 mesh calcium carbonate used as raw materials for plastics factory, rubber factory, paint, waterproof materials plant and exterior paint for building, whiteness more than 85 degrees. 400 mesh to 600 mesh calcium carbonate paste can be used in toothpaste, soap, white degree above 94 degrees. 800 mesh calcium carbonate is used in rubber, plastic, cable, PVC, white degree above 94 degrees. 1250 mesh calcium carbonate can be used in PVC, PE, paint, coating products, paper coating, paper surface coating, white degree above 95 degrees, with characteristics of high purity, high whiteness, non-toxic, odourless, low oil, low hardness, etc..		</p>	
        
		
