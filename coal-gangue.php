        	<h3>Introduction of Coal Gangue</h3>
            <p>
Gangue, also called coal gangue, is the solid waste discharged in coal mining process and coal washing process, black rock harder than coal. Its carbon content is low and exists along with coal seam. Gangue could be found in roof, bottom board, inter-layer, and it could be selected in washing process. The main ingredient are Al2O3, SiO2, Fe2O3, CaO, MgO, Na2O, K2O, P2O5, SO3 and rare elements (gallium, vanadium, titanium, cobalt). Gangue is mixture of carbonaceous material, argillaceous material and arenaceous shale, it has lower calorific value, carbon content reaches to 20%～30%, some of them contain humus acid.</p>
        	<h3>Technological Process of Coal Gangue</h3>
            <p>
Gangue’s hardness is low, it pollutes environment badly, therefore, technology-based machine should be preferential concerned to protect environment. Winnermac gangue powder grinding machine, gangue crusher has many advantages like environmental protection and energy saving, Winnermac gangue grinding mill is applied for processing gangue, barite, limestone, ceramic, slag and other material whose Moh's hardness is less than 9.3. This machine is suitable for mining, metallurgy, chemical engineering, building material and many industries material fine powder making, final product size is adjustable. The gangue grinder is constituted by separator, rollers, grinding table, pressure device, reducer, electromotor, machine shell，etc. According to position of conditioning tower and dust catcher, there are two different layout plans: triple draught fan system and double draught fan system. Rotoclone collector could collect final product, negative pressure of system and gas flow of collector would be reduced.
			</p>			
        	<h3>Application Fields of Coal Gangue</h3>
            <p>
Gangue is mainly applied in gangue cement producing, lightweight aggregate of concrete producing, and refractory brick material producing. In addition, it could be used in coal recycle process, coal and coal gangue power generation, crystallization of aluminum chloride and sodium silicate production and precious rare metal extraction process, it could be used as fertilizer. Meanwhile, gangue could replace clay as raw material of brick making to save fertile farmland, and coal resource could be saved. Gangue could replace clay to be material of cement and lightweight aggregate, and be used to produce low-heat value gas, ceramic, soil conditioner, or applied for road paving, underground filling, ground filling. Grass can also be planted on the hillock and gangue to beautify the environment.
			</p>	
        
		
