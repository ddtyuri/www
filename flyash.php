        	<h3>Introduction of Fly Ash</h3>
            <p>
Fly ash, a type of fine ash collected after coal burning, is the main solid waste discharged by coal-fired power plant. Most of combustible could be burned up in the furnace, incombustible matter in the coal powder is mixed in high temperature smoke. Some of them will be melt under the high temperature, meanwhile because of surface tension, large number of spheroidal particle will be formed. Under the force of air exhausting, smoke with ash will go to trail of furnace. With temperature reducing of smoke, some fine powder will show vitreous state to get potential activity. Before smoke being discharged into atmosphere, spheroidal particle will be collected and separated by dirt catcher, and that is fly ash. The main oxides of fly ash are: SiO2, Al2 O3, FeO, Fe2O3, CaO, and TiO2. With development of electric power industry, fly ash discharge has been increased every year. If fly ash doesn’t get processed, fugitive dust and atmospheric pollution will be caused. If fly ash is discharged to river system, river will be silted up, toxic chemicals in the coal ash will destroy the health of human and other creatures.			</p>
        	<h3>Technological Process of Fly Ash</h3>
            <p>
Utilization of coal ash has a strict demand on material's fineness; therefore, material should be fine grinded. Coal ash grinding machine combined with concentric product's advantages, like high capacity, low consumption, high fineness and reliable performance. It reduces the energy consumption 40% than same specification products and its capacity is higher than others capacity by 20%. European type grinding mill is specific applied in coal ash grinding, European type grinding milling machine combined many years' experience and design, and it's widely applied in cement, electric power, chemical industry, non-metallic mineral industries. Working principle of coal ash: Power of main motor reduced by gear reducer, center shaft and star rack will be forced to rotating, roller will rotate around center shaft under centrifugal force, meanwhile roller do rotation around roller, material could be grinded and crushed in the gap, processed powder will be taken to classifier to be classified, coarse material will fall back to machine, standard powder will be taken to cyclone collector, then final product will be discharged.

			</p>			
        	<h3>Application Fields of Fly Ash</h3>
            <p>
Speed development causes rapid increase of coal ash discharge. Coal ash discharge increases every year. In 1995, coal ash discharge was 125 million tons; in 2000, 150 million tons;in 2010, 300 million tons. Utilization of coal ash could turn waste to treasure, and it has already been a technical and economic policy in construction development and important method to solve environmental pollution, lack of resources problem, and it’s one of the mission of power generation. Coal ash is widely applied in construction, construction material, water conservancy and other industries. Compared with passed situation, the utilization of coal ash has been changed, waste utilization is also concerned as a big part of it. Processed coal ash is applied in roadbed, embankment and concrete admixture, now it is improved to be in cement raw material, cement admixture, key water-control project, pump concrete, concrete product, advanced padding. Adding coal ash in cement could save cement and fine aggregate and water, pumpability and workability of cement will be improved, creeping of concret could be avoid, heat of hydration and thermal dilatability could be decreased, impervious ability of concrete would be increased.			</p>	
        
		
