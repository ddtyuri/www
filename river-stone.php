        	<h3>Introduction of River Stone</h3>
            <p>
River stone, also be called cobble and pebble, one kind of pure nature stone, is exploited from sand hill which appeared as the result of uplifting of riverbed before eons of crustal movement. During tens of thousands of years of vicissitudes, the stones are rounded of the edges due to a flow of water over it, and then buried with sediment under the ground for many years. River stone is mainly composed by silica, the next is a small amount of chemical element like,iron oxide and traces of manganese, copper, aluminum, magnesium, and chemical compound. They have different chromophoric, such as red to blue for iron, copper, purple for manganese, yellow for silica gel stone, jade color with green and so on. Because the element types and content of the chromophoric ion that dissolved in silica hydrothem are different, the stones present in different colors from light to dark. And river stone are in black, white, yellow, red, atrovirens, cinerous and so on.River stones are common nearby sea and river. Black sands gathered on them, and the black sand take up more one half on pebbles. Because widely distributed, good in shape, they are the ideal choice for courtyard and road paving, and building. 
			</p>
        	<h3>Technological Process of River Stone</h3>
            <p>
The greatest feature of river gravel - high hardness and high erosiveness - result in high abrasion of crusher and sand making machine in actual production. Therefore, in order to lower abrasion and cost, the rationality of process configuration of river gravel production line is very important. Currently, the typical configuration includes feeding machine with sieving function, primary crushing employing jaw crusher for large material, second crushing using fine jaw crusher or cone crusher for small and medium-size raw material and the materiel crushing in primary crusher. The second crushed material can be conveyed to shaping equipment (impact crusher/vertical shaft impact crusher). And, the shaped material conveyed to screening equipment for segregating, the returning material will be conveyed to shaping equipment, then the material are in the circulation of crushed.
			</p>			
        	<h3>Application Fields of River Stone</h3>
            <p>
After the process of stone crushing, sand making, and screening, natural river stone would be made into the river sand which is an important industrial mineral raw materials widely used in water conservancy and hydro-power engineering, high level road, highway, railway, passenger line, bridges, airport runways, municipal engineering, high-rise building sand production mechanisms and stone shaping. Moreover, river stone is abundant, cost-efficient, and valuable, widely used as concrete aggregate.
			</p>	
        
		
