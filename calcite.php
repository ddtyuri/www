        	<h3>Introduction of Calcite</h3>
            <p>
Calcite is a kind of widely scattered calcium carbonate mineral in nature. Calcite has many crystal shapes, aggregation of calcite could be crystal, particle, bulk, throatiness, stalactitic or earthy. Calcite is the essential minerals of limestone and marble, it could be applied in many aspects. We all know that karts cave is formed by calcite, and stalactite, white marble are constituted by calcite. Calcite contains Mn, Fe, Zn, Mg, Pb, Sr, Ba, Co, TR substitution; when they reach to a certain level, tetalite, ferrocalcite, zincocalcite, magnesium calcite would be formed. In addition, there are brucite, dolomite, hydroxide and oxide, sulfide of Fe and quartz in the crystal. 
 </p>
 <h3>Technological Process of Calcite</h3>
            <p>
How to process bulk calcite to fine powder in a few microns? The principle is simple. Crusher will process bulk calcite to particle, and then powder grinding machine will process calcite particle to fine powder. Calcite powder grinding system is constituted by electric-vibrating feeder, main grinding mill, powder concentrator, high pressure positive blower, pipeline and dust-removing equipment. Calcite grinding machine system working principle: Material will be taken into grinding mill machine by conveying equipment, grinding force of roller and grinding ring will process material, high pressure draft produced by high pressure positive blower will take final powder to powder concentrator, standard powder will go through concentrator to collector, unqualified powder will be regrinding. Calcite processing machines include high efficiency pulse dust collector, whirlwind powder collector and powder locking machine. Calcite grinding machine adopts qualified powder concentrator, high pressure spring, hook face relieving tool and other patented design, this is the origin of domestic calcite grinding machine technique.
			</p>			
        	<h3>Application Fields of Calcite</h3>
            <p>
Calcite was found in limestone hill, and widely exists in tertiary&quaternary limestone and metamorphic rocks deposits. Calcite is rock-forming mineral of earth, it counts 40% of earth crust, and there are over 200 species of calcite. China, Mexico, England, France, America, Germany are the main countries producing areas of calcite whiteness is less than 94 and more acid non-soluble substance. Calcite could be used as industrial raw material in chemical engineering and cement, and flux in metallurgical industry. It could be processed to make cement and lime, plastic, paper, toothpaste, and additive in food industry. Calcite element could be added into in class industry, and final class product would be translucent to be applied to be glass lamp shade. Deep processed by grinding machine, calcite can be applied in artificial stone, artificial floor tile, natural rubber, synthetic rubber, coating, plastic, new type calcium plastic, cable, paper-making, toothpaste, cosmetic, glass, medicine, oil paint, printing ink, electrical insulation, food, spinning, fodder, binder, aquaseal, pitch, building materials, felt, fire-barrier ceiling and daily-use chemical industries to be filler.			</p>	
        
		
