        	<h3>Introdution of Limestone</h3>
            <p>
Limestone, also known as calcium carbonate, a kind of compound, whose chemical formula is CaCO3, CAS number 34-471-1. It is a common substance on earth, which can be found in the rocks. It is the main component of animal back shell and snail shells, exists in nature by calcite and aragonite the two kinds of minerals. Calcite belongs to trigonal system, it’s the hexagonal crystal, and pure calcite is colorless, it’s white in usual. It’s containing 56% CaO, 44% CO2, the density is 2.715g / cm³, the Mohs’ scale of hardness is 3, it’s brittle. Aragonite belongs to orthorhombic strain, it’s the diamond crystal, and the color is gray or white, the density is 2.94g/cm³, the scale of Mohs’ hardness is 3.5 to 4. In the physical properties of limestone, calcite crystal size is very important. It has a high strength for dense limestone to show the fine crystal structure of low porosity. The density of limestone is 2.65~2.80g/cm³, dolomite limestone is 2.70-2.90g/cm³, and dolomite is 2.85-2.95g/cm³. The bulk density depends on the porosity.
			</p>
        	<h3>Technological Process of Limestone</h3>
            <p>
The common processing technology of limestone is using special grinder mill to mechanical pulverization it into powder, crushing limestone directly can get calcium carbonate. According to the different powder grinding fineness, it's divided into four different specifications on the industry, which are used in various industrial departments. Because limestone reactivity is close contacted with available magnesium content, hardness, grain size and crystalline morphology, improving Improve limestone fineness is an important research direction of technological development in the intra-industry, and it's also an important standard to determine the performance of the milling machine's pros and cons. At present, domestic manufacturers introduced various limestone powder milling equipment are developed from the traditional Raymond mill design process, it's specifically applicable to the deep processing production of limestone, this series flour milling machine uses a more sophisticated process design, can be very well suited for limestone processing. The basic working principle of this type grinder mill is: crusher broken the big lumps of raw material into the needed feed size, and transport the materials into the storage silo by the bucket, then evenly to the host of grinding chamber by electromagnetic vibrating feeder, the shovel and roller scooped the material between the feed mill roll ring toss to form a litter layer, the material is grinding between the grinding roller and grinding ring, in order to achieve the purpose of milling. After grinding the powder flow from the fan to the analysis machine，achieve fineness requirements' powder through the pipe into the large cyclone collector follow the airflow to make separation collection, and then discharged through the discharge is finished powder. Airflow is suction blower by the large cyclone collector return duct. The undesirable powder is thrown out of the outer wall to break away from the air by vanes, coarse fall into the grinding chamber for regrinding because of self-gravity. The airflow system of the machine is of the closed loop, and is in the state of negative pressure circulating. The process technology of limestone is commonly used special mill which mechanically pulverized into powder.
			</p>			
        	<h3>Application Fields of Limestone</h3>
            <p>
Limestone is the highly versatile valuable resource. Limestone in the history of human civilization, with its widely distributed in nature, easily accessible features have been widely used. As an important building material it has a long mining history, in modern industry, limestone is the main raw material manufacture of cement, lime, calcium carbide, and it also the indispensable limestone flux in metallurgical industry, after ultra-fine grinding the high quality limestone is widely used in the manufacturer of paper, rubber, paint, coatings, pharmaceuticals, cosmetics, animal feed, sealing, bonding, etc. According to incomplete statistics, the cement production’s consumption of limestone and building stone, lime production, metallurgical flux, calcium carbonate limestone consumption ratio of 1:3 sum. Limestone is a non-renewable resource, with the continuous progress of science and development of nanotechnology, limestone applications will be further widened.</p>	
        
		
