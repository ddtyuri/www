        	<h3>Introduction of Coal</h3>
            <p>
Coal dust is residue through burned under rated conditions, it’s formed by a series decomposition and chemical combination under a controlled condition, it’s ramification of mineral substance in coal mine. After being heated in vacuum, volatility organic substance will be produced, volatiles is constituted by moisture, hydrocarbon oxide and hydrocarbon, but absorbed water and carbon dioxide are not volatile. Good quality pulverized coal has high volatile, when casting, reducing gas will be produced in cavity, light carbon will be separated out, casting’s surface will be smooth. Volatile should be controlled between 32-38% to avoid pore and short run.
        	<h3>Technological Process of Coal</h3>
            <p>
Coal dust is a type of fine coal powder ground by coal grinding mill, size range 0.05~0.01mm, and most of powder’s size range 20~50μm. Pulverized coal processed by Winnermac grinding machine could be fully burned, use ratio very high, and we have provided technical support for many power plant. In the condition of raw coal’s moisture is less than 15%, Vipeak grinder mill could grind the coal to 200 mesh, drying process could be canceled and energy will be saved. Drying ability could be got from two aspects, when machine working, grinding process will produce heat, meanwhile, hot air would come into machinery from ventilation opening at the bottom of machine, and drying speed will be accelerated. Winnermac grinding machine capacity is high, benefit is high, actual cost is lower. As for power plant, metallurgic plant and other large scale factory, adopting Vipeak coal dust grinder to process coal is the your best choice.
			</p>			
        	<h3>Application Fields of Coal</h3>
            <p>
Green sand mixing with coal dust could prevent casting surface defect, improve surface degree of finish, decrease sand inclusion defects, and improve scatter ability of moulding sand. For wet nodular iron casting, subcutaneous blowhole phenomenon could be avoided, vortex burner could be applied and air preheating is not necessary. Fine coal powder could get burned easier, and complete combustion could be achieve, ash carbon content could be reduced, secondary combustion could be reduced, flame kernel temperature of hearth will be decreased, efficiency will be improved. But high fineness will increase abrasion of machinery parts and especially steel ball maintenance will be increased also. As for power plant, reference value of coal number and electricity will be given.</p>	
        
		
